#include <stdio.h>

int main(void) {


    int I = 1;
    int IV = 4;
    int V = 5;
    int IX = 9;
    int X = 10;
    int XL = 40;
    int L = 50;
    int XC = 90;
    int C = 100;

    for (int i = 1; i <= 100; ++i) {
        
        int N = i;
        printf("%-6d", N);

        while (N >= C){         // C ≡ 100
            printf("%c", 'C');
            N -= C;
        }
        while (N >= L){         // N > L ≡ 50
            if (N >= XC) {
                printf("%c%c", 'X', 'C'); // ^2
                N -= XC;
            }
            else {
                printf("%c", 'L');
                N -= L;
            }
        }
        while (N >= X){         // N > X ≡ 10
            if (N >= XL) {
                printf("%c%c", 'X', 'L'); // ^2
                N -= XL;
            }
            else {
                printf("%c", 'X');
                N -= X;
            }
        }
        while (N >= V){         // N > V ≡ 5
            if (N >= IX) {
                printf("%c%c", 'I', 'X'); // ^2
                N -= IX;
            }
            else {
                printf("%c", 'V');
                N -= V;
            }
        }
        while (N >= I){         // N > I ≡ 1
            if (N >= IV) {
                printf("%c%c", 'I', 'V'); // ^2
                N -= IV;
            }
            else {
                printf("%c", 'I');
                N -= I;
            }
        }
        puts("");
    }


}

